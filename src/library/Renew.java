    private void renewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renewActionPerformed
        int rowcount=jTable1.getSelectedRowCount();
        if(rowcount==0)
        {
            JOptionPane.showMessageDialog(null, "No rows selected yet");
            return;
        }
        try {
            // TODO add your handling code here:

            int n=jTable1.getSelectedRowCount();
            int rows[]=jTable1.getSelectedRows();
            for (int i = 0; i < n; i++) {
                try{
                    String isbn=jTable1.getValueAt(rows[i],0).toString();
                    rs=st.executeQuery("select status from renew where reg=\'"+regno+"\' and isbn="+isbn);
                    rs.next();
                    if(rs.getBoolean("status"))
                    {
                        JOptionPane.showMessageDialog(null, "Book with ISBN: "+isbn+" reached max renewal limit");
                    }
                    else
                    {
                        rs=st.executeQuery("select datediff(date(now()),due_date) from issue where reg=\'"+regno+"\' and isbn="+isbn);
                        rs.next();
                        if(rs.getInt(1)>0)
                        {
                            JOptionPane.showMessageDialog(null, "Book with ISBN: "+isbn+" can't be renewed as you have crossed the due date.\nKindly return the book and issue again");
                        }
                        else if(rs.getInt(1)>-7)
                        {
                            st.execute("update renew set status=1 where reg=\'"+regno+"\' and isbn="+isbn);
                            st.execute("update issue set due_date=date_add(due_date,interval 10 day) where reg=\'"+regno+"\' and isbn="+isbn);
                            
                            
                            rs = st.executeQuery("select * from books where ISBN=" + isbn);
                            rs.next();
                            String ti = rs.getString("Book_Name");
                            String au = rs.getString("Author_Name");
                            rs=st.executeQuery("select * from issue where reg='"+regno+"' and isbn="+isbn);
                            rs.next();
                            String du=rs.getString("due_date");
                            JavaEmail ob = new JavaEmail(email.getText(), "Renewed", "<p><strong>Dear " + name.getText() + "(" + reg.getText() + ")</strong></p>\n"
                                        + "<p>The following items have been renewed:</p>\n"
                                        + "<p>Title: " + ti + "</p>\n"
                                        + "<p>Author: " + au + "</p>\n"
                                        + "<p>ISBN: " + isbn + "</p>\n"
                                        + "<p>Due Date: " + du+ "</p>\n"
                                        + "<p>Thank You ,</p>\n"
                                        + "<p><strong>Librarian, VIT-AP Library.</strong></p>"
                                        + "<p class=\"auto-style1\">\"Use your own ID Card for your own transactions.\"</p>");
                                ob.start();
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null, "Sorry, You cannot renew the book so early");
                        }
                    }
                }
                catch(SQLException e)
                {
                    Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, e);
                }
            }

            Startup obj=new Startup();
            obj.setTitle("StartUp");
            //obj.setLocation(400,400);
            obj.setVisible(true);
            dispose();
        } catch (Exception ex) {
            Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_renewActionPerformed

