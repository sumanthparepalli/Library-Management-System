    public void status() throws SQLException {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
        Statement st = con.createStatement();
        ResultSet rs;
        rs = st.executeQuery("select sum(oquantity) from books");
        rs.next();
        int original = rs.getInt(1);
        rs = st.executeQuery("select sum(iquantity) from books");
        rs.next();
        int issued = rs.getInt(1);
        total.setText(Integer.toString(original));
        issue.setText(Integer.toString(issued));
        rem.setText(Integer.toString(original - issued));
    }
